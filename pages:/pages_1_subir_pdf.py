import streamlit as st
import os

# Carpeta donde se guardar√°n los PDFs
CARPETA_PDFS = "pdfs_subidos"
os.makedirs(CARPETA_PDFS, exist_ok=True)

st.title("üì§ Subir nuevo archivo PDF")

st.markdown("""
Aqu√≠ puedes subir archivos PDF para analizarlos m√°s tarde con el sistema.
""")

# Subida del archivo
archivo_pdf = st.file_uploader("Selecciona un archivo PDF", type=["pdf"])

if archivo_pdf:
    ruta_archivo = os.path.join(CARPETA_PDFS, archivo_pdf.name)

    # Guardar el archivo en la carpeta
    with open(ruta_archivo, "wb") as f:
        f.write(archivo_pdf.read())

    st.success(f"‚úÖ Archivo '{archivo_pdf.name}' guardado correctamente.")
    st.info("Ahora puedes ir a la pesta√±a de preguntas y usar este PDF (cuando lo integremos al √≠ndice).")